    {
        if (request()->ajax()) {
            $query = Custom::query();

            return DataTables::of($query)
                ->editColumn('reference_photo', function ($item) {
                    return '<img style="max-width: 150px" src="' . Storage::url($item->reference_photo) . '"/>';
                })
                ->rawColumns(['reference_photo'])
                ->make();
        }
        return view('pages.dashboard.custom.index');
    }

